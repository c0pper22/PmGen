def get_catalog_for_model(model: str):
    """
    Return a Catalog for a human-readable model string, e.g.
    'TOSHIBA e-STUDIO2515AC' → the 2515/3015/3515 catalog.

    KitLinkRule will call this to build canon→kit mappings.
    """
    if not model:
        return None
    up = (model or "").upper()

    for key, mdl in REGISTRY.items():
        if key in up:
            return getattr(mdl, "catalog", None)

    import re
    m = re.search(r"E[-\s]?STUDIO\s*(\d{4,5}AC)", up)
    if m:
        key = m.group(1)
        mdl = REGISTRY.get(key)
        if mdl:
            return getattr(mdl, "catalog", None)

    return None